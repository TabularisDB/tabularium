import { Marked } from 'marked'
import DOMPurify from 'isomorphic-dompurify'

const marked = new Marked({
  async: false,
  gfm: true,
  breaks: false,
})

const ALLOWED_TAGS = [
  'a',
  'abbr',
  'b',
  'blockquote',
  'br',
  'code',
  'em',
  'h1',
  'h2',
  'h3',
  'h4',
  'h5',
  'h6',
  'hr',
  'i',
  'img',
  'kbd',
  'li',
  'ol',
  'p',
  'pre',
  's',
  'strong',
  'sub',
  'sup',
  'table',
  'tbody',
  'td',
  'th',
  'thead',
  'tr',
  'ul',
  'span',
  'del',
  'div',
  'figure',
  'figcaption',
  'section',
  'article',
  'header',
  'footer',
  'nav',
  'aside',
  'main',
  'details',
  'summary',
  'small',
  'mark',
  'time',
  'picture',
  'source',
  'video',
  'audio',
]

const ALLOWED_ATTR = [
  'href',
  'title',
  'alt',
  'src',
  'srcset',
  'sizes',
  'class',
  'id',
  'rel',
  'target',
  'loading',
  'decoding',
  'width',
  'height',
  'role',
  'open',
  'datetime',
  'poster',
  'controls',
  'autoplay',
  'loop',
  'muted',
  'playsinline',
]

export function renderMarkdown(raw: string): string {
  if (!raw) return ''
  const html = marked.parse(raw) as string
  return DOMPurify.sanitize(html, {
    ALLOWED_TAGS,
    ALLOWED_ATTR,
  })
}
